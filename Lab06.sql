--Question 3: Display the customers name, pid ordered, and the dollars for all orders, sorted by dollars from high to low
select name, pid, dollars
from orders, customers
where orders.cid = customers.cid
order by dollars DESC


--Question 4: Display all customer names in reverse alphabetical order and their total ordered and nothing more. Use coalesce to avoid nulls--


select name, sum(orders.qty) as sum
from customers, orders
where customers.cid = orders.cid
group by customers.cid
order by name desc




