	and orders.cid = customers.cid
	and orders.pid = products.pid

--Question 6: Write a query to check the accuracy of the dollars column in the Orders table. This means comparing Orders.dollars from data in other tables
--and comparing those values to the values in Orders.dollars. Display all rows in Orders where Orders.dollars is incorrect, if any

select (orders.qty * products.priceUSD) - ((orders.qty * products.priceUSD) * (customers.discount * .01)) as totalCustCost
from orders, products, customers
where orders.pid = products.pid
and orders.cid = customers.cid




