--Question 1: Display the name and city of customers who live in any city that makes the most different kinds of products. 
--Return the name and city of customers from either one of those
--City that makes the most products: Dallas and Newark
--name and city of cutsomers that live in Dallas and Newark: Basics, Allied. Nobody lives in newarkd
select name 
from customers
where customers.city in(
	select city
	from(
		select products.city, count(city) as sum
		from products
		group by city
		order by sum desc
		limit 2)
		as alias);



--Question 2: Display the names of products whose priceUSD is below the average priceUSD in alphabetical order
